def Print(text, scale=1, color=(255,255,255)):
    textSur=pygame.Surface((6*len(str(text)),len(symgen[0])), pygame.SRCALPHA)
    for i in range(len(str(text))):
        symbol=pygame.Surface((5,7), pygame.SRCALPHA)
        for y in range(len(symgen[ord(str(text)[i])-32])):
            A = symgen[ord(str(text)[i])-32][y]
            for x in range(4):
                if A%2==1: pygame.draw.rect(symbol,color,(4-x,y,1,1))
                A//=2
            if A==1: pygame.draw.rect(symbol,color,(0,y,1,1))
        textSur.blit(symbol,(6*i,0))    
    return pygame.transform.scale(textSur,(textSur.get_width()*scale,textSur.get_height()*scale))
